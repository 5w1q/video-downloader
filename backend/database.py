import os
from contextlib import contextmanager
from datetime import datetime, timezone

from dateutil.relativedelta import relativedelta
from sqlalchemy import create_engine, text
from sqlalchemy.engine import Engine
from sqlalchemy.exc import IntegrityError

DB_SQLITE_PATH = os.path.join(os.path.dirname(__file__), "data", "app.db")


def _database_url() -> str:
    raw = (os.getenv("DATABASE_URL") or "").strip()
    if raw:
        if raw.startswith("postgres://"):
            return raw.replace("postgres://", "postgresql+psycopg://", 1)
        if raw.startswith("postgresql://"):
            return raw.replace("postgresql://", "postgresql+psycopg://", 1)
        return raw
    os.makedirs(os.path.dirname(DB_SQLITE_PATH), exist_ok=True)
    return f"sqlite:///{DB_SQLITE_PATH.replace(os.sep, '/')}"


def get_db_backend() -> str:
    return "postgresql" if _database_url().startswith("postgresql+") else "sqlite"


def _build_engine() -> Engine:
    db_url = _database_url()
    if db_url.startswith("sqlite"):
        return create_engine(
            db_url,
            future=True,
            connect_args={"check_same_thread": False},
        )
    return create_engine(
        db_url,
        future=True,
        pool_pre_ping=True,
        pool_size=int(os.getenv("DB_POOL_SIZE", "5")),
        max_overflow=int(os.getenv("DB_MAX_OVERFLOW", "5")),
    )


engine = _build_engine()


@contextmanager
def get_db():
    conn = engine.connect()
    tx = conn.begin()
    try:
        yield conn
        tx.commit()
    except Exception:
        tx.rollback()
        raise
    finally:
        conn.close()


