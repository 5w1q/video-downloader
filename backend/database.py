def complete_order(session_id: str, payment_intent_id: str) -> dict | None:
    with get_db() as conn:
        order_row = conn.execute(
            text("SELECT * FROM orders WHERE stripe_session_id = :sid AND status = 'pending'"),
            {"sid": session_id},
        ).fetchone()
        if not order_row:
            return None
        order = order_row._mapping
        now = datetime.now(timezone.utc).isoformat()

        user_row = conn.execute(text("SELECT * FROM users WHERE id = :uid"), {"uid": order["user_id"]}).fetchone()
        if not user_row:
            return None
        user = user_row._mapping

        current_expire = None
        if user.get("vip_expire_at"):
            try:
                current_expire = datetime.fromisoformat(str(user["vip_expire_at"]))
            except ValueError:
                pass

        base_time = datetime.now(timezone.utc)
        if current_expire and current_expire > base_time:
            base_time = current_expire

        if order["plan_type"] == "yearly":
            new_expire = base_time + relativedelta(years=1)
        else:
            new_expire = base_time + relativedelta(months=1)

        conn.execute(
            text(
                """
                UPDATE orders
                SET status = 'paid', stripe_payment_intent_id = :piid, paid_at = :paid_at, updated_at = :updated_at
                WHERE id = :oid
                """
            ),
            {
                "piid": payment_intent_id,
                "paid_at": now,
                "updated_at": now,
                "oid": order["id"],
            },
        )
        conn.execute(
            text(
                """
                UPDATE users
                SET is_vip = 1, vip_expire_at = :vip_expire_at, updated_at = :updated_at
                WHERE id = :uid
                """
            ),
            {
                "vip_expire_at": new_expire.isoformat(),
                "updated_at": now,
                "uid": order["user_id"],
            },
        )
        return dict(order)


